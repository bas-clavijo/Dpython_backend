#Tips Hints 
#Tipados de Python
my_string_variable = "My String variable"
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 5
print(my_string_variable)
print(type(my_string_variable))

#Tipados de FastApi
#se debe de especificar el tipo de la variable 
#al especificar su tipo de dato nos daran diferentes funciones/(".")
my_typed_variable: str = "My typed string variable "
print(my_typed_variable)
print(type(my_typed_variable))

my_typed_variable: int = 5
print(my_typed_variable)
print(type(my_typed_variable))

#tipado debil
my_typed_variable: str = 5
print(my_typed_variable)
print(type(my_typed_variable))

